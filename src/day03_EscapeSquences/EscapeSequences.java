package day03_EscapeSquences;
/*  EscapeSquences: MUST be given with double quote
    \n: starts a new line       yeni satirdan baslatir
    \t: paragraph space(tab)    tab kadar bosluk birakir
    \\: single back slash       slash bir azaltir
    \": double quote            \"   alinti sozler icin kullanilir   basina ve sonuna konur
 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPyton \nC");
        System.out.println("---------------------------------------");
        System.out.println("Hello Cydeo \nHow are you all today? \nIt`s nice to see you all! \nWhat class do we next?");
        System.out.println("-----------------------");
        System.out.println("\tJava is cool programming language.");
        System.out.println("------------------------");
        System.out.println("abcd \\");
        System.out.println("------------------------");
        System.out.println("My favorite TV show is \"Game of Thrones\"");




    }




}
