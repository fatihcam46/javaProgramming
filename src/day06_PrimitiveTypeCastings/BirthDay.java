package day06_PrimitiveTypeCastings;

public class BirthDay {
    public static void main(String[] args) {
        String name1 = "John";
        int birthDay = 25;
        String birthMonth = "April";
        int birthYear = 1995;

        // name was born on month/day/year
        System.out.println(name1 +" was born on "+birthMonth+"/"+birthDay+"/"+birthYear);
        System.out.println("---------------------------------------");
        // My favorite book is "bookname"
        String bookName = "The Rich Dad and The Poor Dad.";
        System.out.println("My favorite book is \""+bookName +"\"");


//
// name , birthday(int), birthMonth (String), birthYear(int)   John was born on April/25/1995.
    }


}
