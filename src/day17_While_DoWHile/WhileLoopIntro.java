package day17_While_DoWHile;

public class WhileLoopIntro {
    public static void main(String[] args) {
        if(true){
            System.out.println("Wooden Spoon");  //true  // if te bir kere kontrol ediyor
        }
        System.out.println("--------------------------------------");
        while (true){  //repeated if
          System.out.println("Wooden Spoon");  //true  // while da sonsuz sayida yapiyor
      }


    }
}
